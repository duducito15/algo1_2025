public class BucleWhile {
    public static void main(String[] args) {
        /*
         * Bucle While, se usa siempre y cuando se cumpla una condicion
         * 
         * while (condicion){
         *  instrucciones / sentencias
         * }
         */
        int i = 1;
        while (i <= 10) {
            System.out.println(i);
            i++;
        }


    }
}
